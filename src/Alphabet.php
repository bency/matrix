    {
        $str_len = strlen($string);
        if ($str_len < 1) {
            return null;
        }
        $max_height = 0;

        // get max height
        for ($i = 0; $i < $str_len; $i++) {
            $alpha = ord($string[$i]);
            $max_height = max($max_height, count(self::$V5ProphitCell[$alpha]));
        }

        for ($row = $max_height - 1; $row >= 0; $row--) {
            for ($i = 0; $i < $str_len; $i++) {
                if (!isset($combo[$row])) {
                    $combo[$row] = [];
